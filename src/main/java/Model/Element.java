package Model;

public class Element {
	public int sizeArray;
	public int[] array;

	public int getSizeArray() {
		return sizeArray;
	}

	public void setSizeArray(int sizeArray) {
		this.sizeArray = sizeArray;
	}

	public int[] getArray() {
		return array;
	}

	public void setArray(int[] array) {
		this.array = array;
	}
}
